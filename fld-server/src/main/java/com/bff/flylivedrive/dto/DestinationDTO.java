package com.bff.flylivedrive.dto;

public class DestinationDTO {
	
	private Long id;
	private Long avioId;
	private Long cityId;
	
	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getAvioId() {
		return avioId;
	}
	public void setAvioId(Long avioId) {
		this.avioId = avioId;
	}
	public Long getCityId() {
		return cityId;
	}
	public void setCityId(Long cityId) {
		this.cityId = cityId;
	}
	
	

}
